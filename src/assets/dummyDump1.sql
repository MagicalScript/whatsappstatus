CREATE TABLE Alimentaire (id INTEGER DEFAULT NULL PRIMARY KEY AUTOINCREMENT,destination TEXT DEFAULT NULL,Categorie TEXT DEFAULT NULL,C REAL DEFAULT NULL,P REAL DEFAULT NULL,L REAL DEFAULT NULL);

CREATE TABLE Obectif (id INTEGER DEFAULT NULL PRIMARY KEY AUTOINCREMENT,type TEXT DEFAULT NULL,periode TEXT DEFAULT NULL,date TEXT DEFAULT NULL);

CREATE TABLE user (id INTEGER DEFAULT NULL PRIMARY KEY AUTOINCREMENT,age INTEGER DEFAULT NULL,height INTEGER DEFAULT NULL,name TEXT DEFAULT NULL,weight INTEGER DEFAULT NULL,gender TEXT DEFAULT NULL, objectif INTEGER DEFAULT NULL);

CREATE TABLE weight (id INTEGER DEFAULT NULL PRIMARY KEY AUTOINCREMENT,weight REAL DEFAULT NULL,date TEXT DEFAULT NULL);

CREATE TABLE DetailRepas (id INTEGER DEFAULT NULL PRIMARY KEY AUTOINCREMENT,Alimentaire INTEGER DEFAULT NULL,NumRepas INTEGER DEFAULT NULL);

CREATE TABLE Repas (id INTEGER DEFAULT NULL PRIMARY KEY AUTOINCREMENT,Objectif INTEGER DEFAULT NULL, date text, NAlm INTEGER, TKal INTEGER);